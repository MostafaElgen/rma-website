<div>Important tips for cleaning marble and granite kitchens</div>
<div>&nbsp;</div>
<div>Cleaning kitchens made of marble and granite should be done periodically, using detergents that do not cause damage, while staying away from caustic materials that cause cracks in the marble, and leave the colors faded.</div>
<div>Here are some important tips that should be followed to clean marble or granite kitchens, which help you maintain the quality, color and luster of walls and floors for as long as possible:</div>
<div>&bull; Use a piece of cloth to clean the stains after placing them in soap mixed with warm water.</div>
<div>&bull; After removing stains or dirt completely, use a piece of cloth to dry the marble, and do not leave it wet.</div>
<div>&bull; Marble is one of the stones that needs special care when dealing with it. It is not permissible to rub it using a brush or other tools that cause abrasion of the surface.</div>
<div>&bull; Avoid using vinegar to clean marble surfaces, because over time it causes fading of colors because it is an acidic substance.</div>
<div>&bull; It is best to use a polisher for marble to give an aesthetic touch to its outer surface.</div>
<div>&bull; You can use baking soda to remove stubborn stains, by placing it on the spots, leaving it a little, and then cleaning the marble with warm water.</div>
